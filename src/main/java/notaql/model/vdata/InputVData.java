package notaql.model.vdata;

import notaql.model.path.InputPath;

/**
 * Executes the input paths
 *
 * @author Thomas Lottermann
 */
public class InputVData implements VData {
    private static final long serialVersionUID = -2168836419143229005L;
    private final InputPath path;

    public InputVData(InputPath path) {
        this.path = path;
    }

    public InputPath getPath() {
        return path;
    }

    @Override
    public String toString() {
        return path.toString();
    }
}
